package com.cinte.reto_travelocity.models;

public class HotelReservationModels {

	private String destination;
	private String initialReservationDate;
	private String finalReservationDate;
	private String adults;
	private String children;
	private String ageChild;
	private String countryCode;
	private String phoneNumber;

	public String getDestination() {
		return destination;
	}

	public String getInitialReservationDate() {
		return initialReservationDate;
	}

	public String getFinalReservationDate() {
		return finalReservationDate;
	}

	public String getAdults() {
		return adults;
	}

	public String getChildren() {
		return children;
	}

	public String getAgeChild() {
		return ageChild;
	}

	public String getCountryCode() {
		return countryCode;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}
}
